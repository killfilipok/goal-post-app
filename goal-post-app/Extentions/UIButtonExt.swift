import UIKit

extension UIButton {
    func setSelectedColor(){
        self.backgroundColor = #colorLiteral(red: 0.05048409849, green: 0.6732526422, blue: 0.2713977695, alpha: 1)
    }
    func setDesekectedColor(){
        self.backgroundColor = #colorLiteral(red: 0.5843137503, green: 0.8235294223, blue: 0.4196078479, alpha: 1)
    }
}

